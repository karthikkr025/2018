package test_package;

public class While_Do_While_Loop {
	
public static void main(String[] args) {
		
		int i =1;
		int j =2;
		int sum = i+j;

//While Loop program
		while (sum<10)
		{
			System.out.println(sum);
			sum++;
			System.out.println("While loop is Executed");
		}
		
		System.out.println("karthik");
	
	
	int a=5;
//Do-While Loop Program	
	do
	{
		System.out.println(a);
		a++;
	}
	while(a<10);
	System.out.println("do while loop Executed");

   }

}
